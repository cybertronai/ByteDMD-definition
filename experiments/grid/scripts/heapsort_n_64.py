def manual_matvec_row(n: int) -> int:
    """y[i] = sum_j A[i][j] * x[j] with x preloaded into a hot scratch
    buffer at the very bottom of the stack.

    Because the Python signature is `matvec(A, x)`, `x` sits at the
    *end* of the arg stack (addrs n²+1..n²+n). Each x[j] is re-read
    n times from those high arg addresses in the original. Preloading
    x into `c_X` at addrs n+3..2n+2 cuts every subsequent x access to
    near-top-of-scratch cost.

      s, tmp  (addrs 1-2)        — accumulator + tmp
      c_X     (addrs 3..n+2)     — hot x buffer (one-time preload)
      y       (addrs n+3..2n+2)  — output"""
    a = _alloc()
    A = a.alloc_arg(n * n); x = a.alloc_arg(n)
    s = a.alloc(1); tmp = a.alloc(1)
    c_X = a.alloc(n)
    y = a.alloc(n)
    a.set_output_range(y, y + n)
    # Preload x once from arg into c_X.
    for j in range(n):
        a.touch_arg(x + j); a.write(c_X + j)
    for i in range(n):
        a.touch_arg(A + i * n + 0); a.touch(c_X + 0)
        a.write(s)
        for j in range(1, n):
            a.touch_arg(A + i * n + j); a.touch(c_X + j)
            a.touch(s); a.touch(tmp)
            a.write(s)
        a.touch(s)
        a.write(y + i)
    a.read_output()
    return a.cost


def manual_matvec_blocked(n: int, B: int = 8) -> int:
    """Optimal Stationary-Accumulator 1D-Blocked matvec
    (gemini/optimal-matvec.md). Sweeps A perfectly left-to-right
    exactly once (arg-stack); holds one B-element x-block in a
    tight L1 cache at addrs 3..B+2; uses a single scalar `s` as
    stationary accumulator at addr 1. Inner loop footprint is
    strictly addrs 1..B+2 — the mathematically minimal spatial
    layout achievable under the semi-ring + polyhedron
    restrictions. For n=64, B=8 this hits 208,832 — the strict
    theoretical floor derived in gemini/optimal-matvec.md.

      s    (addr 1)         — stationary accumulator
      tmp  (addr 2)         — multiply intermediate
      c_x  (addrs 3..B+2)   — current x-block cache
      y    (addrs B+3..)    — main output / partial-sum array"""
    a = _alloc()
    A = a.alloc_arg(n * n)
    x = a.alloc_arg(n)

    s = a.alloc(1)
    tmp = a.alloc(1)
    c_x = a.alloc(B)
    y = a.alloc(n)
    a.set_output_range(y, y + n)

    for j_blk in range(0, n, B):
        # Load one B-element block of x into the L1 cache.
        for j in range(B):
            a.touch_arg(x + j_blk + j)
            a.write(c_x + j)

        for i in range(n):
            # Carry the running sum for row i from the y array.
            if j_blk != 0:
                a.touch(y + i)
                a.write(s)

            # Inner loop physically bounded to addrs 1..B+2.
            for j in range(B):
                a.touch_arg(A + i * n + j_blk + j)
                a.touch(c_x + j)
                # First-ever MAC for this row: write s directly;
                # subsequent MACs accumulate s += tmp.
                if j_blk != 0 or j != 0:
                    a.touch(s)
                    a.touch(tmp)
                a.write(s)

            # Flush the running sum back into y[i].
            a.touch(s)
            a.write(y + i)

    a.read_output()
    return a.cost


def manual_matvec_col(n: int) -> int:
    """Outer loop over j: y[i] += A[i][j] * x[j]. A, x on arg stack;
    tmp, c_xj (scalar cache for x[j]) and y on scratch.

    Earlier version read x[j] only once per j (outside the i-loop) —
    an accumulator-outside undercharge caught by the DSL parity
    check. Fixed by caching x[j] into `c_xj` once per j and re-reading
    from it per inner MAC."""
    a = _alloc()
    A = a.alloc_arg(n * n); x = a.alloc_arg(n)
    tmp = a.alloc(1)
    c_xj = a.alloc(1)
    y = a.alloc(n)
    a.set_output_range(y, y + n)
    for j in range(n):
        a.touch_arg(x + j); a.write(c_xj)   # preload x[j] into hot scalar
        for i in range(n):
            if j == 0:
                # First contribution: y[i] = A[i][0] * x[0] (mul only)
                a.touch_arg(A + i * n + j); a.touch(c_xj); a.write(y + i)
            else:
                # MAC: y[i] += A[i][j] * x[j]  — 4 priced reads
                a.touch_arg(A + i * n + j); a.touch(c_xj); a.write(tmp)
                a.touch(y + i); a.touch(tmp); a.write(y + i)
    a.read_output()
    return a.cost


