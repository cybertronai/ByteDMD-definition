def manual_spatial_convolution(H: int, W: int, K: int) -> int:
    """2D single-channel convolution with priced MAC intermediates.
    Each inner op prices the multiply's tmp result and reads the
    accumulator per inner iteration (ByteDMD has no free register)."""
    a = _alloc()
    Wk = a.alloc_arg(K * K)
    img = a.alloc_arg(H * W)
    tmp = a.alloc(1); s = a.alloc(1)
    out_h = H - K + 1
    out_w = W - K + 1
    O = a.alloc(out_h * out_w)
    a.set_output_range(O, O + out_h * out_w)
    for i in range(out_h):
        for j in range(out_w):
            first = True
            for ki in range(K):
                for kj in range(K):
                    a.touch_arg(img + (i + ki) * W + (j + kj))
                    a.touch_arg(Wk + ki * K + kj)
                    a.write(tmp)
                    if first:
                        a.touch(tmp); a.write(s)
                        first = False
                    else:
                        a.touch(s); a.touch(tmp); a.write(s)
            a.touch(s); a.write(O + i * out_w + j)
    a.read_output()
    return a.cost


def manual_fft_conv(N: int) -> int:
    """Convolution via FFT optimized for the geometric stack model
    (gemini/optimize-fft-conv.md). Four stacked optimizations:

      (1) 2D L1 cache blocking — a 256-point FFT factors into 16x16
          passes so all N·log N butterflies run inside a 16-cell
          cache at addresses 1..16 (the ultra-cheap sqrt(addr) zone).
      (2) Shared geometric workspace — X and Y buffers are multiplexed
          across FFT(X), FFT(Y), and IFFT(Z) instead of allocating
          three separate N-sized regions.
      (3) Fused load/store bit-reversal — arg-stack inputs map directly
          into bit-reversed coordinates on first touch, eliminating
          a full-array swap pass.
      (4) Fused pointwise Z — the IFFT's cache-load step reads
          X[rev_idx] * Y[rev_idx] on-the-fly instead of materializing
          a Z array first.

    Each butterfly is priced as 5 touches (tmp = b; b = a - tmp;
    a = a + tmp) to match the exact L2 footprint of the earlier
    manual."""
    a = _alloc()
    B = 16

    cache_A = a.alloc(B)
    tmp = a.alloc(1)

    X_in = a.alloc_arg(N)
    Y_in = a.alloc_arg(N)

    X = a.alloc(N)
    Y = a.alloc(N)

    rev = [0] * N
    j = 0
    for i in range(1, N):
        bit = N >> 1
        while j & bit:
            j ^= bit
            bit >>= 1
        j ^= bit
        rev[i] = j

    def fft_in_place(base, in_arg=-1, fuse_z=False, write_base=-1):
        if write_base == -1:
            write_base = base

        # Stages 1..4: block size B, contiguous.
        for r in range(B):
            offset = r * B
            for c in range(B):
                idx = offset + c
                if in_arg != -1:
                    a.touch_arg(in_arg + rev[idx])
                elif fuse_z:
                    rev_idx = rev[idx]
                    a.touch(Y + rev_idx)
                    a.touch(X + rev_idx)
                else:
                    a.touch(base + idx)
                a.write(cache_A + c)

            m = 1
            while m < B:
                for k in range(0, B, m * 2):
                    for jj in range(m):
                        idx1 = cache_A + k + jj
                        idx2 = cache_A + k + jj + m
                        a.touch(idx2); a.write(tmp)
                        a.touch(idx1); a.touch(tmp); a.write(idx2)
                        a.touch(idx1); a.touch(tmp); a.write(idx1)
                m *= 2

            for c in range(B):
                a.touch(cache_A + c); a.write(base + offset + c)

        # Stages 5..8: block size B, strided by B.
        for c in range(B):
            for r in range(B):
                a.touch(base + r * B + c); a.write(cache_A + r)

            m = 1
            while m < B:
                for k in range(0, B, m * 2):
                    for jj in range(m):
                        idx1 = cache_A + k + jj
                        idx2 = cache_A + k + jj + m
                        a.touch(idx2); a.write(tmp)
                        a.touch(idx1); a.touch(tmp); a.write(idx2)
                        a.touch(idx1); a.touch(tmp); a.write(idx1)
                m *= 2

            for r in range(B):
                a.touch(cache_A + r); a.write(write_base + r * B + c)

    # FFT(X): load from X_in (fused bit-reversal), final stage parks in Y.
    fft_in_place(X, in_arg=X_in, write_base=Y)
    # FFT(Y): load from Y_in, writes into X (so X now holds Y_fft).
    fft_in_place(X, in_arg=Y_in)
    # Lock X as the Z output region.
    a.set_output_range(X, X + N)
    # IFFT(Z) fused with pointwise Z = X_fft * Y_fft.
    fft_in_place(X, fuse_z=True)
    a.read_output()
    return a.cost


def manual_regular_convolution(H: int, W: int, K: int, Cin: int, Cout: int) -> int:
    """Full multi-channel CNN layer with a rolling K-row image cache.

    Layout strategy (under the sqrt(addr) cost model):
      - Wk stays on the arg stack at its natural low arg-addresses
        (addrs 1..K*K*Cin*Cout); it is hot (every output cell reads every
        kernel weight) but already cheap because the arg stack has its
        own origin at 1.
      - img stays on the arg stack too, but each img row is copied ONCE
        into a tiny rolling K-row buffer on scratch at addrs right above
        the scalar accumulator. That moves the K*K*Cin reads per (i,j,co)
        from high arg addresses (~sqrt(1000)) to low scratch addresses
        (~sqrt(200)).
      - O sits on scratch above the row buffer so that the epilogue
        read_output() still sees the output at modest addresses.
    """
    a = _alloc()
    Wk = a.alloc_arg(K * K * Cin * Cout)
    img = a.alloc_arg(H * W * Cin)
    out_h = H - K + 1
    out_w = W - K + 1

    # Low-scratch layout: tmp, accumulator, then K-row rolling img buffer.
    tmp = a.alloc(1); s = a.alloc(1)
    row_stride = W * Cin
    buf = a.alloc(K * row_stride)
    O = a.alloc(out_h * out_w * Cout)
    a.set_output_range(O, O + out_h * out_w * Cout)

    def load_row(r: int) -> None:
        slot = buf + (r % K) * row_stride
        base_arg = img + r * row_stride
        for x in range(row_stride):
            a.touch_arg(base_arg + x)
            a.write(slot + x)

    for r in range(K):
        load_row(r)

    for i in range(out_h):
        if i > 0:
            load_row(i + K - 1)

        slot_base = [buf + ((i + ki) % K) * row_stride for ki in range(K)]

        for j in range(out_w):
            for co in range(Cout):
                # MAC with priced intermediates per inner op.
                first = True
                for ki in range(K):
                    sb = slot_base[ki]
                    for kj in range(K):
                        col_off = (j + kj) * Cin
                        wk_base = Wk + ((ki * K + kj) * Cin) * Cout + co
                        for ci in range(Cin):
                            a.touch(sb + col_off + ci)
                            a.touch_arg(wk_base + ci * Cout)
                            a.write(tmp)
                            if first:
                                a.touch(tmp); a.write(s)
                                first = False
                            else:
                                a.touch(s); a.touch(tmp); a.write(s)
                a.touch(s); a.write(O + (i * out_w + j) * Cout + co)
    a.read_output()
    return a.cost


