def manual_naive_matmul(n: int) -> int:
    """Truly naive triple loop C[i][j] = Σₖ A[i][k] · B[j][k] with NO
    scratchpad caching at all. A and B stay on the arg stack; the
    accumulator is C[i][j] itself (no scalar hoist). The inner MAC
    prices (mul's tmp intermediate + per-k C re-read):

      tmp     (addr 1)        — multiply intermediate (only scratchpad)
      C       (addrs 2..)     — output, accumulator-in-place

    Use this as a "what does no caching cost?" baseline; compare with
    `manual_naive_matmul_cached` (A-row hoisted)."""
    a = _alloc()
    A = a.alloc_arg(n * n); B = a.alloc_arg(n * n)
    tmp = a.alloc(1)
    C = a.alloc(n * n)
    a.set_output_range(C, C + n * n)
    for i in range(n):
        for j in range(n):
            for k in range(n):
                # mul: tmp = A[i][k] * B[j][k]   (2 arg reads, free write)
                a.touch_arg(A + i * n + k)
                a.touch_arg(B + j * n + k)
                a.write(tmp)
                if k == 0:
                    # first MAC: C[i][j] = tmp
                    a.touch(tmp); a.write(C + i * n + j)
                else:
                    # accumulate: C[i][j] += tmp
                    a.touch(C + i * n + j); a.touch(tmp)
                    a.write(C + i * n + j)
    a.read_output()
    return a.cost


def manual_naive_2d_tiled_matmul(n: int, T: int = 4) -> int:
    """Naive matmul with output-only 2D tiling — partitioned matrix
    tiling, no scratchpad caching. The (i, j) loop is tile-blocked
    (bi → bj → ii → jj → k) but each C[i][j] is still fully accumulated
    over all k before moving on, and A/B rows stay on the arg stack
    for every MAC.

      tmp (addr 1)            — multiply intermediate (only scratchpad)
      C   (addrs 2..n²+1)     — output, accumulated in place

    Address multiplicities are identical to `manual_naive_matmul`, so
    the fixed-placement cost is the same (177,744 at n=16). The point
    of this variant is to isolate the effect of loop reordering on
    recency-based heuristics: LRU-style `bytedmd_live` /
    `bytedmd_classic` see shorter A/B reuse distances inside each
    (bi, bj) tile than in the row-major naive sweep. Contrast with
    `manual_naive_tiled_matmul` (same tiling but with sA/sB scratchpad
    slabs that actually cut arg traffic)."""
    assert n % T == 0, "T must divide n"
    a = _alloc()
    A = a.alloc_arg(n * n); B = a.alloc_arg(n * n)
    tmp = a.alloc(1)
    C = a.alloc(n * n)
    a.set_output_range(C, C + n * n)
    for bi in range(0, n, T):
        for bj in range(0, n, T):
            for ii in range(T):
                for jj in range(T):
                    i = bi + ii
                    j = bj + jj
                    for k in range(n):
                        a.touch_arg(A + i * n + k)
                        a.touch_arg(B + j * n + k)
                        a.write(tmp)
                        if k == 0:
                            a.touch(tmp); a.write(C + i * n + j)
                        else:
                            a.touch(C + i * n + j); a.touch(tmp)
                            a.write(C + i * n + j)
    a.read_output()
    return a.cost


def manual_naive_tiled_matmul(n: int, k: int = 4) -> int:
    """Naive matmul where each block caches k rows of A and k rows of
    B (k·n cells each) and computes k² entries against those two
    scratch slabs. Output block (bi, bj) covers k² C entries at
    C[bi·k..bi·k+k-1, bj·k..bj·k+k-1]; each entry is a full n-wide
    dot product against rows of sA and sB.

      tmp (addr 1)               — multiply intermediate
      sA  (addrs 2..k·n+1)       — k rows of A
      sB  (addrs k·n+2..2k·n+1)  — k rows of B
      C   (above sB)             — output / accumulator in place

    For n=16, a sweep of k ∈ {1, 2, 4, 8, 16} picks k=2 (154,384)
    as the sweet spot. Smaller k is a near-no-op (k=1: 177,688
    essentially ties truly-naive); larger k bloats the scratch
    footprint faster than it amortizes arg reloads (k=16 caches
    both matrices entirely but pays ~246k because sA/sB reads then
    cost sqrt(512) each). The chosen k balances fewer arg reloads
    against keeping the scratch tail cheap."""
    assert n % k == 0, "k must divide n"
    nb = n // k
    a = _alloc()
    A = a.alloc_arg(n * n); B = a.alloc_arg(n * n)
    tmp = a.alloc(1)
    sA = a.alloc(k * n)
    sB = a.alloc(k * n)
    C = a.alloc(n * n)
    a.set_output_range(C, C + n * n)
    for bi in range(nb):
        # Load k rows of A once per bi.
        for ii in range(k):
            for l in range(n):
                a.touch_arg(A + (bi * k + ii) * n + l)
                a.write(sA + ii * n + l)
        for bj in range(nb):
            # Load k rows of B once per (bi, bj).
            for jj in range(k):
                for l in range(n):
                    a.touch_arg(B + (bj * k + jj) * n + l)
                    a.write(sB + jj * n + l)
            # k² full-length dot products against the two scratch slabs.
            for ii in range(k):
                for jj in range(k):
                    i_out = bi * k + ii
                    j_out = bj * k + jj
                    for l in range(n):
                        a.touch(sA + ii * n + l)
                        a.touch(sB + jj * n + l)
                        a.write(tmp)
                        if l == 0:
                            a.touch(tmp); a.write(C + i_out * n + j_out)
                        else:
                            a.touch(C + i_out * n + j_out); a.touch(tmp)
                            a.write(C + i_out * n + j_out)
    a.read_output()
    return a.cost


def manual_naive_matmul_cached(n: int) -> int:
    """Naive triple loop C[i][j] = Σₖ A[i][k] · B[j][k], with the
    current A-row hoisted into a hot scratchpad.

    Because A[i][*] is reused across all n values of j (for fixed i),
    preloading it into `c_A_row` once per outer i iteration cuts n−1
    redundant arg-stack reads per A cell down to zero. B[j][*] isn't
    cached (it would need to be reloaded for each i, wiping the win).

      s       (addr 1)         — accumulator
      c_A_row (addrs 2..n+1)   — hot A[i][*] row buffer
      C       (addrs n+2..)    — output"""
    a = _alloc()
    A = a.alloc_arg(n * n); B = a.alloc_arg(n * n)
    tmp = a.alloc(1); s = a.alloc(1)
    c_A_row = a.alloc(n)
    C = a.alloc(n * n)
    a.set_output_range(C, C + n * n)
    for i in range(n):
        # Load A[i][*] into c_A_row once per outer i.
        for k in range(n):
            a.touch_arg(A + i * n + k); a.write(c_A_row + k)
        for j in range(n):
            # MAC with priced intermediates: every k reads tmp and s.
            for k in range(n):
                a.touch(c_A_row + k)
                a.touch_arg(B + j * n + k)
                a.write(tmp)
                if k == 0:
                    a.touch(tmp)
                    a.write(s)
                else:
                    a.touch(s); a.touch(tmp)
                    a.write(s)
            a.touch(s)
            a.write(C + i * n + j)
    a.read_output()
    return a.cost


def manual_tiled_matmul(n: int, T: int | None = None) -> int:
    """Optimal register-blocked, B-row stationary outer product
    (gemini/optimized-tiled-matmul.md + gemini/optimize-tiling-to-death.md).
    Loads a row of B into an L1 vector and a single element of A into a
    scalar register, then updates two 4×4 blocks of C simultaneously to
    maximize the reuse of the fetched B row.

    Two micro-optimizations relative to the B-row-stationary baseline
    (saves exactly 512 cost units → 68,270 → 67,758, the provable
    lower bound under AM-GM for this scratchpad geometry):
      (a) Allocate c_A (4,096 touches) BEFORE tmp (3,840 touches) so
          the hotter scalar locks in at address 1 (cost 1 per read).
      (b) On the first MAC (bk == 0 and kk == 0) bypass tmp entirely:
          write the mul's result directly into sC instead of the
          tmp → sC round-trip, saving 256 tmp reads.

      c_A (addr 1)       — hottest scalar (freq-first)
      tmp (addr 2)       — multiply intermediate
      c_B (addr 3..T+2)  — L1 vector holding current B row
      sC  (addr T+3..)   — 2D L1 scratchpad (blocks*T*T cells)
      C   (just above sC) — output matrix
    """
    if T is None:
        T = max(1, int(round(n ** 0.5)))
    a = _alloc()
    A = a.alloc_arg(n * n)
    B = a.alloc_arg(n * n)

    # Frequency-first allocation: c_A (4096 touches) → addr 1.
    c_A = a.alloc(1)
    tmp = a.alloc(1)
    c_B = a.alloc(T)
    blocks = 2
    sC = a.alloc(blocks * T * T)
    C = a.alloc(n * n)
    a.set_output_range(C, C + n * n)

    for bj in range(0, n, T):
        for bi_start in range(0, n, blocks * T):
            for bk in range(0, n, T):
                for kk in range(min(T, n - bk)):
                    # Stream a single row of B into the L1 vector.
                    for jj in range(min(T, n - bj)):
                        a.touch_arg(B + (bk + kk) * n + (bj + jj))
                        a.write(c_B + jj)
                    # Accumulate across multiple vertical tiles.
                    for bi in range(bi_start,
                                    min(n, bi_start + blocks * T), T):
                        local_bi = (bi - bi_start) // T
                        for ii in range(min(T, n - bi)):
                            a.touch_arg(A + (bi + ii) * n + (bk + kk))
                            a.write(c_A)
                            for jj in range(min(T, n - bj)):
                                # multiply: read c_A, c_B
                                a.touch(c_A)
                                a.touch(c_B + jj)
                                if bk == 0 and kk == 0:
                                    # First MAC: bypass tmp, write
                                    # mul's result directly into sC.
                                    a.write(sC + local_bi * T * T + ii * T + jj)
                                else:
                                    # accumulate: tmp = mul, sC += tmp
                                    a.write(tmp)
                                    a.touch(sC + local_bi * T * T + ii * T + jj)
                                    a.touch(tmp)
                                    a.write(sC + local_bi * T * T + ii * T + jj)

            # Flush the fully computed C tiles back once per (bj, bi_start).
            for bi in range(bi_start, min(n, bi_start + blocks * T), T):
                local_bi = (bi - bi_start) // T
                for ii in range(min(T, n - bi)):
                    for jj in range(min(T, n - bj)):
                        a.touch(sC + local_bi * T * T + ii * T + jj)
                        a.write(C + (bi + ii) * n + (bj + jj))

    a.read_output()
    return a.cost


def manual_rmm(n: int, T: int = 4) -> int:
    """Recursive (cache-oblivious) matmul with T-tile scratchpad at leaves.
    A, B on arg stack; sA/sB/sC/C on scratch. MAC convention: fast_C
    read once per (i,j) outside k-loop; bulk read of fast_C before MAC;
    on first write of a C-tile, skip pre-load of pC (fresh)."""
    a = _alloc()
    A = a.alloc_arg(n * n); B = a.alloc_arg(n * n)
    tmp = a.alloc(1)
    sA = a.alloc(T * T); sB = a.alloc(T * T); sC = a.alloc(T * T)
    C = a.alloc(n * n)
    a.set_output_range(C, C + n * n)
    # is_first is checked against the PREVIOUS compute_tile's C coordinate
    # only — if the C tile loaded in fast_C still matches, skip the pC
    # pre-load. Matches strassen_trace's Scratchpad.sync cache semantic.
    last_C = [None]

    def compute_tile(rA: int, cA: int, rB: int, cB: int, rC: int, cC: int) -> None:
        is_first = (last_C[0] != (rC, cC))
        last_C[0] = (rC, cC)
        # Load A, B tiles (read from arg stack, write to scratch)
        for ii in range(T):
            for jj in range(T):
                a.touch_arg(A + (rA + ii) * n + cA + jj)
                a.write(sA + ii * T + jj)
        for ii in range(T):
            for jj in range(T):
                a.touch_arg(B + (rB + ii) * n + cB + jj)
                a.write(sB + ii * T + jj)
        # MAC with priced intermediates: every kk reads tmp and sC.
        for ii in range(T):
            for jj in range(T):
                for kk in range(T):
                    a.touch(sA + ii * T + kk)
                    a.touch(sB + kk * T + jj)
                    a.write(tmp)
                    if kk == 0 and is_first:
                        a.touch(tmp)
                        a.write(sC + ii * T + jj)
                    else:
                        a.touch(sC + ii * T + jj)
                        a.touch(tmp)
                        a.write(sC + ii * T + jj)
        # Flush fast_C -> pC
        for ii in range(T):
            for jj in range(T):
                a.touch(sC + ii * T + jj)
                if not is_first:
                    a.touch(C + (rC + ii) * n + cC + jj)
                a.write(C + (rC + ii) * n + cC + jj)

    def recurse(rA: int, cA: int, rB: int, cB: int, rC: int, cC: int, sz: int) -> None:
        if sz <= T:
            compute_tile(rA, cA, rB, cB, rC, cC)
            return
        h = sz // 2
        for dr, dc, erb, ecb, frc, fcc in [
            (0, 0, 0, 0, 0, 0), (0, 0, 0, h, 0, h),
            (h, 0, 0, h, h, h), (h, 0, 0, 0, h, 0),
            (h, h, h, 0, h, 0), (h, h, h, h, h, h),
            (0, h, h, h, 0, h), (0, h, h, 0, 0, 0),
        ]:
            recurse(rA + dr, cA + dc, rB + erb, cB + ecb, rC + frc, cC + fcc, h)

    recurse(0, 0, 0, 0, 0, 0, n)
    a.read_output()
    return a.cost


def manual_strassen(n: int, T: int = 4) -> int:
    """Standard Strassen. A, B on arg stack (at top); SA/SB/M/sA/sB/sC/C on
    scratch. Recursion threads is_arg flags through since sub-products
    flip pointers back to scratch buffers (M-intermediates)."""
    a = _alloc()
    A = a.alloc_arg(n * n); B = a.alloc_arg(n * n)
    tmp = a.alloc(1)
    sA = a.alloc(T * T); sB = a.alloc(T * T); sC = a.alloc(T * T)
    C = a.alloc(n * n)
    a.set_output_range(C, C + n * n)

    def _t(addr: int, is_arg: bool) -> None:
        (a.touch_arg if is_arg else a.touch)(addr)

    def add_mats(p1: int, s1: int, is1a: bool,
                 p2: int, s2: int, is2a: bool, h: int) -> None:
        for i in range(h):
            for j in range(h):
                _t(p1 + i * s1 + j, is1a)
                _t(p2 + i * s2 + j, is2a)

    def compute_tile(pA: int, sAstr: int, isAarg: bool,
                     pB: int, sBstr: int, isBarg: bool,
                     pC: int, sCstr: int) -> None:
        for ii in range(T):
            for jj in range(T):
                _t(pA + ii * sAstr + jj, isAarg)
                a.write(sA + ii * T + jj)
        for ii in range(T):
            for jj in range(T):
                _t(pB + ii * sBstr + jj, isBarg)
                a.write(sB + ii * T + jj)
        for ii in range(T):
            for jj in range(T):
                a.touch(pC + ii * sCstr + jj)
                a.write(sC + ii * T + jj)
        # MAC with priced intermediates: every kk reads tmp and sC.
        for ii in range(T):
            for jj in range(T):
                for kk in range(T):
                    a.touch(sA + ii * T + kk)
                    a.touch(sB + kk * T + jj)
                    a.write(tmp)
                    a.touch(sC + ii * T + jj)
                    a.touch(tmp)
                    a.write(sC + ii * T + jj)
        for ii in range(T):
            for jj in range(T):
                a.touch(sC + ii * T + jj)
                a.write(pC + ii * sCstr + jj)

    def recurse(pA_: int, sAstr: int, isAa: bool,
                pB_: int, sBstr: int, isBa: bool,
                pC_: int, sCstr: int, sz: int) -> None:
        if sz <= T:
            compute_tile(pA_, sAstr, isAa, pB_, sBstr, isBa, pC_, sCstr)
            return
        h = sz // 2
        ckpt = a.push()
        SA = a.alloc(h * h); SB = a.alloc(h * h)
        M = [a.alloc(h * h) for _ in range(7)]

        A11, A12 = pA_, pA_ + h
        A21, A22 = pA_ + h * sAstr, pA_ + h * sAstr + h
        B11, B12 = pB_, pB_ + h
        B21, B22 = pB_ + h * sBstr, pB_ + h * sBstr + h

        add_mats(A11, sAstr, isAa, A22, sAstr, isAa, h)
        add_mats(B11, sBstr, isBa, B22, sBstr, isBa, h)
        recurse(SA, h, False, SB, h, False, M[0], h, h)

        add_mats(A21, sAstr, isAa, A22, sAstr, isAa, h)
        recurse(SA, h, False, B11, sBstr, isBa, M[1], h, h)

        add_mats(B12, sBstr, isBa, B22, sBstr, isBa, h)
        recurse(A11, sAstr, isAa, SB, h, False, M[2], h, h)

        add_mats(B21, sBstr, isBa, B11, sBstr, isBa, h)
        recurse(A22, sAstr, isAa, SB, h, False, M[3], h, h)

        add_mats(A11, sAstr, isAa, A12, sAstr, isAa, h)
        recurse(SA, h, False, B22, sBstr, isBa, M[4], h, h)

        add_mats(A21, sAstr, isAa, A11, sAstr, isAa, h)
        add_mats(B11, sBstr, isBa, B12, sBstr, isBa, h)
        recurse(SA, h, False, SB, h, False, M[5], h, h)

        add_mats(A12, sAstr, isAa, A22, sAstr, isAa, h)
        add_mats(B21, sBstr, isBa, B22, sBstr, isBa, h)
        recurse(SA, h, False, SB, h, False, M[6], h, h)

        # Assemble C quadrants. M[*] and pC_ are both scratch.
        def read_M(*indices: int) -> None:
            for i in range(h):
                for j in range(h):
                    for idx in indices:
                        a.touch(M[idx] + i * h + j)

        def write_quadrant(qr: int, qc: int) -> None:
            for i in range(h):
                for j in range(h):
                    a.write(pC_ + (qr + i) * sCstr + (qc + j))

        read_M(0, 3, 4, 6); write_quadrant(0, 0)
        read_M(2, 4);        write_quadrant(0, h)
        read_M(1, 3);        write_quadrant(h, 0)
        read_M(0, 1, 2, 5);  write_quadrant(h, h)

        a.pop(ckpt)

    recurse(A, n, True, B, n, True, C, n, n)
    a.read_output()
    return a.cost


def manual_fused_strassen(n: int, T: int = 4) -> int:
    """Zero-Allocation Fused Strassen (ZAFS): A, B on arg stack; fast
    scratchpads + output C on scratch. Sub-additions are fused into the
    L1 tile loads. Inner MAC correctly prices intermediate multiplication
    products and per-k accumulator reads (gemini/strassen-cheating-macc.md)."""
    a = _alloc()
    A = a.alloc_arg(n * n); B = a.alloc_arg(n * n)
    fast_tmp = a.alloc(1)
    fast_A = a.alloc(T * T); fast_B = a.alloc(T * T); fast_C = a.alloc(T * T)
    C = a.alloc(n * n)
    a.set_output_range(C, C + n * n)

    def compute_fused_tile(ops_A, ops_B, ops_C, r, c, k_off):
        # 1. Fused load A tile (arg) into fast_A (scratch)
        for i in range(T):
            for j in range(T):
                for _sgn, rb, cb in ops_A:
                    a.touch_arg(A + (rb + r + i) * n + cb + k_off + j)
                a.write(fast_A + i * T + j)
        # 2. Fused load B tile (arg) into fast_B (scratch)
        for i in range(T):
            for j in range(T):
                for _sgn, rb, cb in ops_B:
                    a.touch_arg(B + (rb + k_off + i) * n + cb + c + j)
                a.write(fast_B + i * T + j)
        # 3. Tile MAC with priced intermediates
        for i in range(T):
            for j in range(T):
                for k in range(T):
                    # multiply: read A, B → write fast_tmp (free)
                    a.touch(fast_A + i * T + k)
                    a.touch(fast_B + k * T + j)
                    a.write(fast_tmp)
                    if k == 0 and k_off == 0:
                        # first-iter: no prior accumulator; assign sC = tmp
                        a.touch(fast_tmp)
                        a.write(fast_C + i * T + j)
                    else:
                        # accumulate: read tmp + sC → write sC
                        a.touch(fast_C + i * T + j)
                        a.touch(fast_tmp)
                        a.write(fast_C + i * T + j)
        # 4. Fan-out fast_C -> multiple C targets with signs
        for _sgn, rb, cb, is_first in ops_C:
            for i in range(T):
                for j in range(T):
                    a.touch(fast_C + i * T + j)
                    if not is_first:
                        a.touch(C + (rb + r + i) * n + cb + c + j)
                    a.write(C + (rb + r + i) * n + cb + c + j)

    h = n // 2
    q11, q12, q21, q22 = (0, 0), (0, h), (h, 0), (h, h)
    recipes = [
        ([(1, *q11), (1, *q22)], [(1, *q11), (1, *q22)], [(1, *q11, True), (1, *q22, True)]),
        ([(1, *q21), (1, *q22)], [(1, *q11)],            [(1, *q21, True), (-1, *q22, False)]),
        ([(1, *q11)],            [(1, *q12), (-1, *q22)], [(1, *q12, True), (1, *q22, False)]),
        ([(1, *q22)],            [(1, *q21), (-1, *q11)], [(1, *q11, False), (1, *q21, False)]),
        ([(1, *q11), (1, *q12)], [(1, *q22)],             [(-1, *q11, False), (1, *q12, False)]),
        ([(1, *q21), (-1, *q11)], [(1, *q11), (1, *q12)], [(1, *q22, False)]),
        ([(1, *q12), (-1, *q22)], [(1, *q21), (1, *q22)], [(1, *q11, False)]),
    ]
    for A_ops, B_ops, C_ops in recipes:
        for r, c in [(0, 0), (0, T), (T, 0), (T, T)]:
            compute_fused_tile(A_ops, B_ops, C_ops, r, c, k_off=0)
            C_ops_accum = [(sgn, rb, cb, False) for sgn, rb, cb, _ in C_ops]
            compute_fused_tile(A_ops, B_ops, C_ops_accum, r, c, k_off=T)
    a.read_output()
    return a.cost


